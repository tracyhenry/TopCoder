long long BinaryCards::largestNumber(long long A, long long B) 
{
	ULL ans = B, cur = 1;
	for (int i = 0; i < 63; i ++)
	{
		if (cur <= B)
		{
			ULL tmp = ((ULL) B - cur) / (cur * 2 + 2) * (cur * 2 + 2);
			if (tmp + cur >= (ULL) A && tmp + cur < (ULL) B)
				ans = max(ans, tmp + cur * 2 + 1);
		}
		cur = cur * 2 + 1;
	}
	return ans;
}



